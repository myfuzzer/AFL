/* 简单的哈希函数 - 来自 Bob Jenkins */
u32 hash32(const void* key, u32 len, u32 seed) {

  const u8* data = (const u8*)key;
  u32 h1 = seed;
  u32 c1 = 0xcc9e2d51;
  u32 c2 = 0x1b873593;
  u32 i;

  for (i = 0; i < len; i++) {
    
    u32 k1 = data[i];
    k1 *= c1;
    k1 = (k1 << 15) | (k1 >> 17);
    k1 *= c2;

    h1 ^= k1;
    h1 = (h1 << 13) | (h1 >> 19);
    h1 = h1 * 5 + 0xe6546b64;
    
  }

  h1 ^= len;
  h1 ^= h1 >> 16;
  h1 *= 0x85ebca6b;
  h1 ^= h1 >> 13;
  h1 *= 0xc2b2ae35;
  h1 ^= h1 >> 16;

  return h1;
}